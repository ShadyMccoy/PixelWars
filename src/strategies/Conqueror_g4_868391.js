import { sumStrength } from "../core/Army.js";
import Conqueror from "./Conqueror.js";

const BONUS = 1.4;

// Stencil5 cell -> [primary dir, secondary dir]. Primary is the
// dominant-axis step (W=0, E=1, N=2, S=3); secondary is the off-axis
// step (or -1 if the cell sits exactly on one axis).
const DIR_HINTS = (() => {
  const out = new Array(25);
  for (let i = 0; i < 5; i++) {
    for (let j = 0; j < 5; j++) {
      const dy = i - 2;
      const dx = j - 2;
      if (dx === 0 && dy === 0) { out[i * 5 + j] = [-1, -1]; continue; }
      const horiz = dx < 0 ? 0 : 1;
      const vert = dy < 0 ? 2 : 3;
      let primary, secondary;
      if (Math.abs(dx) > Math.abs(dy)) {
        primary = horiz;
        secondary = dy === 0 ? -1 : vert;
      } else if (Math.abs(dy) > Math.abs(dx)) {
        primary = vert;
        secondary = dx === 0 ? -1 : horiz;
      } else {
        primary = horiz;
        secondary = vert;
      }
      out[i * 5 + j] = [primary, secondary];
    }
  }
  return out;
})();

function tryCommit(army, target, sLimit, pid) {
  const tArmies = target.armies;
  let friendlyArmy = null;
  let enemy = 0;
  for (let k = 0; k < tArmies.length; k++) {
    const a = tArmies[k];
    if (a.player.id === pid) friendlyArmy = a;
    else enemy += a.strength;
  }
  if (enemy > 0) {
    const needed = enemy / BONUS + 0.6;
    if (needed > sLimit) return false;
    army.attack(target, needed);
    return true;
  }
  if (friendlyArmy) {
    if (friendlyArmy.strength >= friendlyArmy.maxStrength - 0.5) return false;
    const room = friendlyArmy.maxStrength - friendlyArmy.strength;
    const power = Math.min(sLimit, room);
    if (power <= 0.5) return false;
    army.attack(target, power);
    return true;
  }
  army.attack(target, sLimit);
  return true;
}

// Conqueror_g3 dominated season #10 with no recorded losses. The only
// thing that looked tunable on review was the 5x5 fallback's target
// preference: parent picks the WEAKEST beatable enemy and only uses
// distance as a tiebreak when strength matches exactly. On a wrap map
// with maxArmy 6 and floating-point strengths, exact ties between
// distinct enemy stacks are essentially never observed, so distance
// is dead weight in practice — the bot will chase a weak enemy two
// tiles away in preference to a slightly stronger one right next door.
//
// Flipping the priority to closest-first (weakest as the tiebreak)
// gets pressure onto the nearest enemy a tick earlier on average.
// On a 24x18 wrap map with growth 1.8 those saved ticks compound:
// every match is a long sequence of small exchanges, and engaging
// the closer target first means the army is back in adjacent-mode
// (handled by Conqueror.act, which the parent already trusts) sooner.
//
// Same beatability threshold, same primary/secondary axis logic,
// same tryCommit, same tech as the parent. Only the comparator in
// the stencil5 selection loop changes.
export default {
  name: "Conqueror_g4_868391",
  author: "claude",
  version: 1,
  description: "Conqueror_g3 with closest-first 5x5 fallback target selection.",
  summary: `Parent Conqueror_g3_51d626 went undefeated in season #10,
so this descendant is a small nudge rather than a rewrite. The only
soft spot in the parent is its 5x5 fallback target ordering: it
prefers weakest beatable enemy with distance as a tiebreak that
almost never fires (strengths are floats, ties are rare). That means
the bot will sometimes route toward a weak enemy two tiles off when
a slightly heavier one is one tile off and just as beatable.

Closest-first reordering hands the engagement to the nearer target,
which on lab1 (24x18 wrap, growth 1.8, maxArmy 6) gets the army
back into adjacent-mode (Conqueror.act) one tick sooner on average.
Beatability gate is unchanged. Primary/secondary axis logic is
unchanged. Tech is unchanged. The bot never picks a target the
parent wouldn't have considered; it just picks among them in a
different order.`,
  tech: { move: 90, stack: 0, prod: 2, atk: 4, def: 4 },
  act(army, game) {
    const tile = army.tile;
    if (!tile) return;
    const neighbors = tile.neighbors;
    const pid = army.player.id;
    const sLimit = army.attackPower;

    // Defer to Conqueror whenever any adjacent move is viable: free
    // kill, empty grab, or a friendly with room to be balanced toward.
    let hasAdjacentTarget = false;
    for (let i = 0; i < 4; i++) {
      const t = neighbors[i];
      if (!t) continue;
      const armies = t.armies;
      if (armies.length === 0) { hasAdjacentTarget = true; break; }
      let friendlyArmy = null;
      let enemy = 0;
      for (let k = 0; k < armies.length; k++) {
        const a = armies[k];
        if (a.player.id === pid) friendlyArmy = a;
        else enemy += a.strength;
      }
      if (enemy > 0) {
        const needed = enemy / BONUS + 0.6;
        if (needed <= sLimit) { hasAdjacentTarget = true; break; }
        continue;
      }
      if (friendlyArmy && friendlyArmy.strength < friendlyArmy.maxStrength - 0.5) {
        hasAdjacentTarget = true;
        break;
      }
    }
    if (hasAdjacentTarget) {
      Conqueror.act(army, game);
      return;
    }

    // Stalled - look 2 deep for a beatable enemy. Closest-first
    // (weakest as tiebreak) — see header comment.
    if (!tile.stencil5 || sLimit <= 0.5) return;
    const stencil = tile.stencil5;
    const viewer = army.player;

    let bestPrim = -1;
    let bestSec = -1;
    let bestDist = Infinity;
    let bestEnemy = Infinity;
    for (let i = 0; i < 25; i++) {
      const hints = DIR_HINTS[i];
      if (hints[0] < 0) continue;
      const t = stencil[i];
      if (!t) continue;
      const enemy = -sumStrength(t.armies, viewer);
      if (enemy <= 0) continue;
      if (enemy / BONUS > sLimit + 0.5) continue;
      const dy = (i / 5) | 0;
      const dx = i - dy * 5;
      const dist = Math.abs(dx - 2) + Math.abs(dy - 2);
      if (dist < bestDist || (dist === bestDist && enemy < bestEnemy)) {
        bestDist = dist;
        bestEnemy = enemy;
        bestPrim = hints[0];
        bestSec = hints[1];
      }
    }
    if (bestPrim < 0) return;

    const primaryTarget = neighbors[bestPrim];
    if (primaryTarget && tryCommit(army, primaryTarget, sLimit, pid)) return;
    if (bestSec < 0) return;
    const secondaryTarget = neighbors[bestSec];
    if (secondaryTarget) tryCommit(army, secondaryTarget, sLimit, pid);
  },
};
